    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        // TODO add your handling code here:
        LivroDao dao = new LivroDao();
        System.out.print("Nome:" + txtBuscarLivro.getText());
        try {
            livro = dao.buscarLivroNome(txtBuscarLivro.getText());
            if(livro != null){
                txtNomeDoLivro.setText(livro.getNomeDoLivro());
                txtAutor.setText(livro.getAutor());
                txtAnoEdicao.setText(String.valueOf(livro.getAnoDeEdicao()));
                txtDataDeCadastro.setText(String.valueOf(livro.getDataDeCadastro()));
                txtNotaUsuario.setText(livro.getNotaDoUsuario());
                txtResenha.setText(livro.getResenha());
                BufferedImage upload = ImageIO.read(livro.getImagemLivro());
                Image foto = upload.getScaledInstance(185, 232, Image.SCALE_SMOOTH);
                txtImagemLivro.setIcon(new ImageIcon(foto));
                txtImagemLivro.setText("");
                
            }else{
                JOptionPane.showMessageDialog(this, "Livro não encontrado.");
            }
        } catch (IOException ex) {
            Logger.getLogger(TelaCadastro.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(TelaCadastro.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_btnBuscarActionPerformed

