    public void obterDados(){
        livro.setNomeDoLivro(txtNomeDoLivro.getText());
        livro.setAutor(txtAutor.getText());
        livro.setAnoDeEdicao(Integer.valueOf(txtAnoEdicao.getText()));
        Date data = new Date();
        livro.setDataDeCadastro(new Timestamp(data.getTime()));
        livro.setNotaDoUsuario(txtNotaUsuario.getText());
        livro.setResenha(txtResenha.getText());
        File imagemFile = fileChooser.getSelectedFile();
        if(imagemFile != null){
            livro.setImagemLivro(imagemFile);
        }
        
        
    }
    
    //Metodo para salvar dados no banco.
    public void salvarLivro(){
        
        obterDados();
        //Inserir no banco.
        LivroDao dao = new LivroDao();
        try {
            if(dao.inserirLivro(livro)){
                JOptionPane.showMessageDialog(this, "Cadastrado com sucesso.");
            }else{
                
            }
        } catch (SQLException ex) {
            Logger.getLogger(TelaCadastro.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
    public void deletarLivro(){
        
        LivroDao dao = new LivroDao();    
        try{
            boolean ok = dao.excluir(txtNomeDoLivro.getText());
            if(ok){
                JOptionPane.showMessageDialog(this, "Deletado com sucesso");
            }else{
                JOptionPane.showMessageDialog(this, "Não pode ser deletado");
            }
        }catch(SQLException e){
            
        }
    }
    
    public void alterarLivro(){
        obterDados();
        LivroDao dao = new LivroDao();
        try{
            if(dao.alterar(livro)){
            JOptionPane.showMessageDialog(this, "Alterado com sucesso.");
        }else{
            JOptionPane.showMessageDialog(this, "Alteração não efetuada.");
        }
        }catch(SQLException ex){
            
        }
        
    }

