    private void btnProcuraImagemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnProcuraImagemActionPerformed
        // TODO add your handling code here:

        fileChooser = new JFileChooser();
        fileChooser.setDialogTitle("Procurar imagem");
        fileChooser.setFileSelectionMode(JFileChooser.FILES_ONLY);
        //Filtro para imagem.
        FileNameExtensionFilter filter = new FileNameExtensionFilter("imagem", "jpg","jpeg","png");
        fileChooser.setFileFilter(filter);
        int retorno = fileChooser.showOpenDialog(this);
        if(retorno == JFileChooser.APPROVE_OPTION){
            try {
                BufferedImage upload = ImageIO.read(fileChooser.getSelectedFile());
                Image foto = upload.getScaledInstance(185, 232, Image.SCALE_SMOOTH);
                txtImagemLivro.setIcon(new ImageIcon(foto));
                livro.processarImagem(fileChooser.getSelectedFile().getPath());
                System.out.println(txtImagemLivro.getText());
            } catch (IOException ex) {
                Logger.getLogger(TelaCadastro.class.getName()).log(Level.SEVERE, null, ex);
            }
            
        }
    }//GEN-LAST:event_btnProcuraImagemActionPerformed

